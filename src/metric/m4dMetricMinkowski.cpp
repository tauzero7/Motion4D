bool MetricMinkowski::calcDerivs(const double y[], double dydx[])
{
    dydx[0] = y[4];
    dydx[1] = y[5];
    dydx[2] = y[6];
    dydx[3] = y[7];

    dydx[4] = 0.0;
    dydx[5] = 0.0;
    dydx[6] = 0.0;
    dydx[7] = 0.0;

    return true;
}

bool MetricMinkowski::calcDerivsPar(const double y[], double dydx[])
{
    dydx[0] = y[4];
    dydx[1] = y[5];
    dydx[2] = y[6];
    dydx[3] = y[7];

    dydx[4] = 0.0;
    dydx[5] = 0.0;
    dydx[6] = 0.0;
    dydx[7] = 0.0;

    for (int n = 0; n < 4; n++) {
        dydx[8 + 4 * n + 0] = 0.0;
        dydx[8 + 4 * n + 1] = 0.0;
        dydx[8 + 4 * n + 2] = 0.0;
        dydx[8 + 4 * n + 3] = 0.0;
    }

    return true;
}

double MetricMinkowski::testConstraint(const double y[], const double kappa)
{
    double cm = 1.0 / mSpeedOfLight;

    // Scale the directions with the speed of light before doubling them !!
    double dt = y[4];
    double dx = y[5] * cm;
    double dy = y[6] * cm;
    double dz = y[7] * cm;

    double sum = -kappa;
    sum += -dt * dt + dx * dx + dy * dy + dz * dz;
    return sum;
}

bool MetricMinkowski::transToTwoPlusOne(vec4 p, vec4& cp)
{
    cp = vec4(p[0], p[1], p[2], p[0]);
    return true;
}

