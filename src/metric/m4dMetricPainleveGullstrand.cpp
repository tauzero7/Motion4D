/*! Calculate the contravariant metric components at position 'pos'.
 *
 *  \param pos : pointer to position.
 */
bool MetricPainleveGullstrand::calculateMetric(const double* pos) {
    double r     = pos[1];
    double theta = pos[2];

    double c  = mSpeedOfLight;

    double t2 = 1 / r;
    double t4 = c * c;
    double t7 = sqrt(rs * t2);
    double t8 = c * t7;
    double t9 = r * r;
    double t10 = sin(theta);
    double t11 = t10 * t10;

    g_compts[0][0] = -(-rs + r) * t2 * t4;
    g_compts[0][1] = t8;
    g_compts[0][2] = 0.0;
    g_compts[0][3] = 0.0;
    g_compts[1][0] = t8;
    g_compts[1][1] = 1.0;
    g_compts[1][2] = 0.0;
    g_compts[1][3] = 0.0;
    g_compts[2][0] = 0.0;
    g_compts[2][1] = 0.0;
    g_compts[2][2] = t9;
    g_compts[2][3] = 0.0;
    g_compts[3][0] = 0.0;
    g_compts[3][1] = 0.0;
    g_compts[3][2] = 0.0;
    g_compts[3][3] = t9 * t11;

    return true;
}

/*! Calculate the Christoffel symbols of the second kind at position 'pos'.
 *
 *  \param pos : pointer to position.
 */
bool MetricPainleveGullstrand::calculateChristoffels(const double* pos) {
    double r     = pos[1];
    double theta = pos[2];

    double c  = mSpeedOfLight;

    double t1 = 1 / r;
    double t3 = sqrt(rs * t1);
    double t5 = r * r;
    double t7 = rs / t5;
    double t9 = c * t3 * t7 / 2.0;
    double t10 = -rs + r;
    double t14 = c * c;
    double t18 = t7 / 2.0;
    double t19 = 1 / c;
    double t24 = t19 * t3;
    double t26 = sin(theta);
    double t28 = cos(theta);
    double t29 = 1 / t26 * t28;
    double t30 = t26 * t26;

    christoffel[0][0][0] = t9;
    christoffel[0][0][1] = t10 / t5 / r * t14 * rs / 2.0;
    christoffel[0][0][2] = 0.0;
    christoffel[0][0][3] = 0.0;
    christoffel[0][1][0] = t18;
    christoffel[0][1][1] = -t9;
    christoffel[0][1][2] = 0.0;
    christoffel[0][1][3] = 0.0;
    christoffel[0][2][0] = 0.0;
    christoffel[0][2][1] = 0.0;
    christoffel[0][2][2] = 0.0;
    christoffel[0][2][3] = 0.0;
    christoffel[0][3][0] = 0.0;
    christoffel[0][3][1] = 0.0;
    christoffel[0][3][2] = 0.0;
    christoffel[0][3][3] = 0.0;
    christoffel[1][0][0] = t18;
    christoffel[1][0][1] = -t9;
    christoffel[1][0][2] = 0.0;
    christoffel[1][0][3] = 0.0;
    christoffel[1][1][0] = t19 / t3 * t7 / 2.0;
    christoffel[1][1][1] = -t18;
    christoffel[1][1][2] = 0.0;
    christoffel[1][1][3] = 0.0;
    christoffel[1][2][0] = 0.0;
    christoffel[1][2][1] = 0.0;
    christoffel[1][2][2] = t1;
    christoffel[1][2][3] = 0.0;
    christoffel[1][3][0] = 0.0;
    christoffel[1][3][1] = 0.0;
    christoffel[1][3][2] = 0.0;
    christoffel[1][3][3] = t1;
    christoffel[2][0][0] = 0.0;
    christoffel[2][0][1] = 0.0;
    christoffel[2][0][2] = 0.0;
    christoffel[2][0][3] = 0.0;
    christoffel[2][1][0] = 0.0;
    christoffel[2][1][1] = 0.0;
    christoffel[2][1][2] = t1;
    christoffel[2][1][3] = 0.0;
    christoffel[2][2][0] = -t24 * r;
    christoffel[2][2][1] = -t10;
    christoffel[2][2][2] = 0.0;
    christoffel[2][2][3] = 0.0;
    christoffel[2][3][0] = 0.0;
    christoffel[2][3][1] = 0.0;
    christoffel[2][3][2] = 0.0;
    christoffel[2][3][3] = t29;
    christoffel[3][0][0] = 0.0;
    christoffel[3][0][1] = 0.0;
    christoffel[3][0][2] = 0.0;
    christoffel[3][0][3] = 0.0;
    christoffel[3][1][0] = 0.0;
    christoffel[3][1][1] = 0.0;
    christoffel[3][1][2] = 0.0;
    christoffel[3][1][3] = t1;
    christoffel[3][2][0] = 0.0;
    christoffel[3][2][1] = 0.0;
    christoffel[3][2][2] = 0.0;
    christoffel[3][2][3] = t29;
    christoffel[3][3][0] = -t24 * r * t30;
    christoffel[3][3][1] = -t10 * t30;
    christoffel[3][3][2] = -t26 * t28;
    christoffel[3][3][3] = 0.0;

    return true;
}

